// conversion of human age to dog year

//  variable named myAge, and set it equal to your age as a number.
let myAge = 21;

//  Create a variable named earlyYears, variable value will change in future.
let earlyYears = 2;

earlyYears = earlyYears * 10.5;

laterYears = myAge - 2;

laterYears = laterYears * 4;

console.log(`earlyYears : ${earlyYears} & laterYears : ${laterYears}`);

let myAgeInDogYears = earlyYears + laterYears;

let myName = "Vishal";
myName = myName.toLowerCase();

// final statement 
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);





